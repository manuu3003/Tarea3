package com.example.mgn03793.tarea3;
public class Animal {
    private String nombre;
    private String fecha;
    private int tipo; // true : femenino, false : masculino

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String telefono) {
        this.fecha = telefono;
    }

    public int getTipo() {
        return tipo;
    }

    public void setTipo(int tipo) {
        this.tipo = tipo;
    }
}